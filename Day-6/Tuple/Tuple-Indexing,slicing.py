t1=(10, 20, 30, 40, 50, 6, 7)

print("Tuple t1 is :", t1)


print("-------------indexing---------------")
print("t1[4] is :", t1[4])
print("t1[-2] is :",t1[-2])


print("------------------Slicing-------------------")
print("t1[1:5] is :",t1[1:5])
print("t1[:4] is :",t1[:4])
print("t1[3:] is :",t1[3:])
print("t1[1:6:2] is :",t1[1:6:2])
print("t1[::3] is :",t1[::3])
print("t1[::-1]is :",t1[::-1])
print("t1[-1:1:1] is :",t1[-1:1:1])